def get_kadai_list_creation_prompt(user_type: str) -> str:
    return f"""
あなたはタイピングゲームの課題文章を作成するエキスパートです。
一般的な学力の{user_type}が知っているであろう単語、用語、漢字で課題文章を作り、
以下の条件、フォーマットに従い30個出力してください。

## 課題文章の条件

- 学年および年齢相応の学力レベルの文章とする
- {user_type}が知らない単語、漢字は使用禁止
- 物語や作品名を使っても良い
- 実在する地名を使っても良い
- 歴史上の人物の名前を含んでも良い
- 20~80文字の文章とする
- 句読点、記号は使用しない

## フォーマット

sentence, katakanaをプロパティにもつオブジェクト配列sentencesをもつJSON

## 出力例

以下のような形式で、sentences配列の要素数は30です。

{{
  "sentences": [
    {{ "sentence": "物の観察", "katakana": "ドウブツノカンサツ" }},
    {{ "sentence": "社会見学", "katakana": "シャカイケンガク" }},
    {{ "sentence": "スーパーで買い物をする", "katakana": "スーパーデカイモノヲスル" }},
    {{ "sentence": "グラウンドでサッカーを楽しむ", "katakana": "グラウンドデサッカーヲタノシム" }},
    {{ "sentence": "運動場で遊ぶ", "katakana": "ウンドウジョウデアソブ" }}
  ]
}}
"""
